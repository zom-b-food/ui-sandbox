package com.activation;

public class Activate4 {
	private String mainnewno;
	private String mainnotoport;


	public Activate4() {
		mainnewno = "";
		mainnotoport = "";

	}


	public Activate4(String pmainnewno, String pmainnotoport
	) {
		mainnewno = pmainnewno;
		mainnotoport = pmainnotoport;


	}

	public void setmainnewno(String gg) {
		mainnewno = gg;
	}

	public String getmainnewno() { return mainnewno; }


	public void setmainnotoport(String hh) {
		mainnotoport = hh;
	}

	public String getmainnotoport() { return mainnotoport; }


}
